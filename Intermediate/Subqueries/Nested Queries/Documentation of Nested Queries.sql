SQL Nested Queries
A nested query (or subquery) is an SQL query placed inside another query to make the overall operation more structured and readable. 
The inner query executes first and its result is then used by the outer query. Subqueries can appear in the SELECT, FROM or WHERE clauses, making them useful for tasks such as filtering, 
aggregation and retrieving data based on the results of another query. They are helpful for breaking down complex SQL operations into smaller, structured steps that are easier to read and maintain.
